package de.squig.plc.logic.objects.guiFunctions;

public class GuiFunction {
	private short idx;
	private String name;

	public GuiFunction(short idx, String name) {
		super();
		this.idx = idx;
		this.name = name;
	}

	
	
	
	
	public short getIdx() {
		return idx;
	}





	public String getName() {
		return name;
	}
	
	
	
}
